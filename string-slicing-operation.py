print(str1[0:])  # it will print the all the content from 0th index
print(str1[:4]) #it will print the content from 0 to 3 index
print(str1[0:-7]) # it will print the content from 0 to [length-7]th (0 to 24) index
print(str1[-3:-1]) # it will print the content from [length-3]th to [length-1]th (28 to 31) index

